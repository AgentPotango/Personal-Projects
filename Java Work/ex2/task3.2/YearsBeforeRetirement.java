public class YearsBeforeRetirement
{
  public static void main(String[] args)
  {
    int myCurrentAge = 18;
    int retirementAge = 68;
    int yearsLeftWorking = retirementAge - myCurrentAge;
    System.out.println("My age now is " + myCurrentAge);
    System.out.println("I will retire at the age of " + retirementAge);
    System.out.println("Years left working is " + yearsLeftWorking);
  }
}
     
